public class Ships {
    int size;
    String letter;
    int x;
    int y;


    public int getShipSize() {
        return this.size;
    }

    public String getShipLetter() {
        return this.letter;
    }

    public void setPosition(int posX, int posY) {
        this.X = posX;
        this.Y = posY;
    }

    public int getX() {
        return this.X;
    } 

    public int getY() {
        return this.Y;
    }

}